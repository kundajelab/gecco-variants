rm(list=ls())
library(ggplot2)
data=read.table("gecco.snps.flora.svm.scores.txt",header=TRUE,sep='\t',row.names=1)
means=colMeans(data)
std=apply(as.matrix(data),2,sd)
summary=rbind(means,std)
write.table(summary,file="mean_and_std.tsv",quote=FALSE,sep='\t',row.names=TRUE,col.names=TRUE)
data=as.data.frame(scale(data))
write.table(round(data,3),file="gecco.snps.flora.svm.scores.scaled.txt",quote=FALSE,row.names=TRUE,col.names=TRUE,sep='\t')
